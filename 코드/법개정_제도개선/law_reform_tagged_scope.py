# 3. 데이터 처리 및 대수별 저장 (전체 회차 집계)
# -------------------------------------------------------------------
def process_and_save():
    if not IN_TAGGED_CSV.exists():
        print(f"❌ 파일을 찾을 수 없습니다: {IN_TAGGED_CSV}")
        return

    # 전체 기간(20~22대) 데이터 로드
    df = pd.read_csv(IN_TAGGED_CSV)
    
    # 회차 필터용 날짜 표준화
    df['meeting_date'] = pd.to_datetime(df['date'].astype(str).str.split('(').str[0].str.strip(), 
                                        format="%Y년 %m월 %d일", errors='coerce').dt.strftime('%Y-%m-%d')

    df_filtered = df[df["is_law_reform"] == 1].copy()
    
    # 상세 분류 수행
    df_filtered["law_reform_scope"] = df_filtered["speech_text"].apply(tag_law_reform_scope)
    df_filtered["policy_l3"] = df_filtered.apply(tag_policy_l3, axis=1)
    
    # 분류된 행만 추출 및 L2 매핑
    df_valid = df_filtered[df_filtered["policy_l3"].notna()].copy()
    df_valid["policy_l2"] = df_valid["policy_l3"].map(L3_TO_L2)
    
    # 대수별로 순회하며 별도 저장
    for assembly_no in [20, 21, 22]:
        sub_df = df_valid[df_valid["assembly_no"] == assembly_no].copy()
        
        sub_df['party'] = sub_df['party'].fillna("정보없음").str.strip()
        sub_df = sub_df[~sub_df["party"].isin(["미분류", "정보없음"])]
        
        if sub_df.empty:
            continue

        # 회차/회의별 그룹화 (대시보드 필터용)
        sub_df = sub_df.rename(columns={"session": "session_no"})
        
        stats = (
            sub_df.groupby([
                "session_no", "meeting_no", "meeting_date", 
                "party", "policy_l2", "policy_l3", "law_reform_scope"
            ])
            .size()
            .reset_index(name="count")
        )

        output_path = DATA_DIR / f"law_reform_scope_stats_{assembly_no}.csv"
        stats.to_csv(output_path, index=False, encoding="utf-8-sig")
        
        unique_l3 = stats['policy_l3'].unique()
        print(f"✅ 제{assembly_no}대 처리 완료: {len(stats)}개 데이터 행 저장 (L3 분야 수: {len(unique_l3)}개)")

if __name__ == "__main__":
    print("🚀 정책 분야 전체 분석 가동 (20대~22대 통합 처리)...")
    process_and_save()